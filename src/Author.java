public class Author {
    String NameAuthor;
    String FamousAuthor;

    public Author(String name, String famous) {
        this.NameAuthor = name;
        this.FamousAuthor = famous;
    }
    public String getNameAuthor() {
        return this.NameAuthor;
    }
    public String getFamousAuthor() {
        return this.FamousAuthor;
    }
}
